package kr.co.schedule;

/**
 * 스케쥴 저장 클래스
 */
public class Schedule {
	private int order;
	private String subject;

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

}
